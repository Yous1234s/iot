package modele;

/**
 *
 * @author TOSHIBA
 */
public class Zone {
    private int idZone;
    private String nom;

    public Zone() {
    }

    public Zone(int idZone, String nom) {
        this.idZone = idZone;
        this.nom = nom;
    }

    public int getIdZone() {
        return idZone;
    }

    public void setIdZone(int idZone) {
        this.idZone = idZone;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
}
